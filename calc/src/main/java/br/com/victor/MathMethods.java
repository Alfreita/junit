package br.com.victor;

public class MathMethods {

	
	public int soma(int a,int b) {
		int soma;
		soma=a+b;
		return soma;
		
	}
	public int sub(int a,int b) {
		int soma;
		soma= a-b;
		return soma;
	}
	public int mult(int a,int b) {
		int soma;
		soma=a*b;
		return soma;
	}
	public int div(int a,int b) {
		int soma;
		soma=a/b;
		return soma;
	}
}
